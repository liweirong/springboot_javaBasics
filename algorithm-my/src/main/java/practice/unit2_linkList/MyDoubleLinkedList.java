public class MyDoubleLinkedList {
    public Node head, tail;
    public Integer size;

    static class Node {
        public Integer val;
        public Node pre;
        public Node next;

        public Node() {
        }

        public Node(Integer val) {
            this.val = val;
        }
    }

    public MyDoubleLinkedList() {
        head = new Node();
        tail = new Node();
        head.val = 0;
        tail.val = 0;
        head.next = tail;
        tail.pre = head;
        this.size = 0;
    }


    public int get(int index) {
        if (index >= size || index < 0) {
            return -1;
        }
        Node head = this.head.next;
        for (int i = 0; i <index ; i++) {
            head = head.next;
        }
        return head.val;
    }

    public void addAtHead(int val) {
        addAtIndex(0, val);
    }

    public void addAtTail(int val) {
        addAtIndex(size, val);
    }

    public void addAtIndex(int index, int val) {
        if (index > size) return;
        if (index < 0) index = 0;
        ++size;

        Node find = this.head.next;
        for (int i = 0; i < index; i++) {
            find = find.next;
        }
        Node add = new Node(val);
        // æ‰¾åˆ°ä½ç½®è¿›è¡Œäº¤æ¢
        Node pre = find.pre;
        pre.next = add;
        add.pre = pre;
        add.next = find;
        find.pre = add;
    }

    public void deleteAtIndex(int index) {
        if (index >= size) return;
        if (index < 0) index = 0;
        --size;
        Node find = this.head.next;
        for (int i = 0; i < index; i++) {
            find = find.next;
        }
        // æ‰¾åˆ°ä½ç½®è¿›è¡Œäº¤æ¢
        find.next.pre = find.pre;
        find.pre.next = find.next;

        find.next = null;
        find.pre = null;

    }




}

